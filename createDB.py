def createDB(kb):
    is_a_pairs = [
        ("Тварина", "Еукаріот"), # child and parent
        ("Рослина", "Еукаріот"),

        # Тварини
        ("Хребетна", "Тварина"),
        ("Безхребетна", "Тварина"),

        # Тварини - хребетні
        ("Ссавець", "Хребетна"),
        ("Риба", "Хребетна"),

        ("Наземний", "Ссавець"),
        ("Водний", "Ссавець"),
        ("Річкова", "Риба"),
        ("Морська", "Риба"),

        ("Слон", "Наземний"),
        ("Тигр", "Наземний"),
        ("Кит", "Водний"),
        ("Дельфін", "Водний"),
        ("Короп", "Річкова"),
        ("Карась", "Річкова"),
        ("Баракуда", "Морська"),
        ("Риба-місяць", "Морська"),

        # Тварини - безхребетні
        ("Черв", "Безхребетна"),
        ("Медуза", "Безхребетна"),

        ("Кільчастий", "Черв"),
        ("Круглий", "Черв"),
        ("Гідроїдна", "Медуза"),
        ("Сцифоїдна", "Медуза"),

        ("Дощовий", "Кільчастий"),
        ("П'явка", "Кільчастий"),
        ("Аскариди", "Круглий"),
        ("Паразит", "Круглий"),
        ("Португальський кораблик", "Гідроїдна"),
        ("Морська оса", "Гідроїдна"),
        ("Коренерот", "Сцифоїдна"),
        ("Лева грива", "Сцифоїдна"),

        # Рослини
        ("Судинна", "Рослина"),
        ("Несудинна", "Рослина"),

        # Рослини судинні
        ("Голонасінна", "Судинна"),
        ("Покритонасінна", "Судинна"),

        ("Хвойна", "Голонасінна"),
        ("Нехвойна", "Голонасінна"),
        ("Однодольна", "Покритонасінна"),
        ("Дводольна", "Покритонасінна"),

        ("Сосна", "Хвойна"),
        ("Ялина", "Хвойна"),
        ("Туя", "Нехвойна"),
        ("Ялівець", "Нехвойна"),
        ("Пшениця", "Однодольна"),
        ("Кукурудза", "Однодольна"),
        ("Троянда", "Дводольна"),
        ("Яблуня", "Дводольна"),

        # Рослини несудинні
        ("Мохоподібна", "Несудинна"),
        ("Водорость", "Несудинна"),

        ("Мох", "Мохоподібна"),
        ("Горицвіт", "Мохоподібна"),
        ("Зелена", "Водорость"),
        ("Бура", "Водорость"),

        ("Сфагнум", "Мох"),
        ("Торф'яний", "Мох"),
        ("Весняний", "Горицвіт"),
        ("Літній", "Горицвіт"),
        ("Плеврокок", "Зелена"),
        ("Хлорела", "Зелена"),
        ("Ламінарія", "Бура"),
        ("Фукус", "Бура"),
    ]

    # [child] is_a [parent]
    all_entities = set() # унікальні назви сутностей з is_a_pairs
    for child, parent in is_a_pairs:
        all_entities.add(child)
        all_entities.add(parent)

    for name in sorted(all_entities): # створили ці сутності в БД
        kb.add_object(name)

    for part, whole in is_a_pairs:
        kb.add_relation(part, "is_a", whole)

    # [частина] part_of [ціле]
    part_entities = ["Хвіст", "Лапа", "Шерсть", "Плавник", "Листок", "Корінь", "Хвоя"]
    for name in part_entities:
        kb.add_object(name)

    part_of_relations = [
        ("Хвіст", "Ссавець"),
        ("Хвіст", "Риба"),
        ("Шерсть", "Наземний"),
        ("Плавник", "Водний"),
        ("Лапа", "Ссавець"),
        ("Листок", "Судинна"),
        ("Корінь", "Рослина"),
        ("Хвоя", "Хвойна"),
    ]
    for part, whole in part_of_relations:
        kb.add_relation(part, "part_of", whole)

    # 3) [середовище] is_habitat_of [X]
    habitats = ["Ліс", "Річка", "Море", "Сад"]
    for h in habitats:
        kb.add_object(h)

    lives_in_relations = [
        ("Ссавець", "Ліс"),
        ("Хвойна", "Ліс"),
        ("Річкова", "Річка"),
        ("Водорость", "Річка"),
        ("Морська", "Море"),
        ("Водний", "Море"),
        ("Дводольна", "Сад"),
        ("Мохоподібна", "Сад"),
    ]
    for subj, place in lives_in_relations:
        kb.add_relation(place, "is_habitat_of", subj)

    # Слон не має шерсті (хоча він Наземний → Ссавець → має Шерсть)
    kb.add_exception("Шерсть", "part_of", "Слон")

    # Водні ссавці не мають лап
    kb.add_exception("Лапа", "part_of", "Водний")
    kb.add_exception("Лапа", "part_of", "Кит")
    kb.add_exception("Лапа", "part_of", "Дельфін")

    # Водні ссавці не живуть у лісі
    kb.add_exception("Ліс", "is_habitat_of", "Водний")
    kb.add_exception("Ліс", "is_habitat_of", "Кит")
    kb.add_exception("Ліс", "is_habitat_of", "Дельфін")